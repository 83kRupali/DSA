import java.util.PriorityQueue;

public class PriorityQueuee {
    public static void main(String[] args) {
        PriorityQueue<Integer> pq = new PriorityQueue<>();
        pq.add(10);
        pq.add(20);
        pq.add(30);
        System.out.println(pq);
        pq.remove();
        System.out.println(pq);

        
    }
}
